public class _3_Check_Odd_or_Even_Number {
    public static void main(String[] args) {
        int n = 22;

        if (n % 2 == 0) {
            System.out.println("the number " + n + " is even");
        } else {
            System.out.println("the number " + n + " is odd");
        }
    }
}
